const db = require('../config/database');

async function upsert(items) {
  for (const [id, name, description, price, image] of items) {
    const existing = await db.getAsync('SELECT id FROM products WHERE id = ?', [id]);
    if (!existing) {
      await db.runAsync(
        'INSERT INTO products (id, name, description, price, image, stock) VALUES (?, ?, ?, ?, ?, ?)',
        [id, name, description, price, image, 100]
      );
    } else {
      await db.runAsync(
        'UPDATE products SET name = ?, description = ?, price = ?, image = ? WHERE id = ?',
        [name, description, price, image, id]
      );
    }
  }
}

